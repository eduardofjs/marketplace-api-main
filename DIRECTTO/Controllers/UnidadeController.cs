        [Route("api/Unidade/CadastroUnidade")]
        [HttpPut]
        public HttpResponseMessage CadastroUnidade(Unidade objUnidade)
        {
            if (ModelState.IsValid)
            {
                try
                {
                    if (objUnidade.UNI_Id > 0)
                    {
                        var fUpdate = _UnidadeAppServices.UpdateUnidade(objUnidade);
                        if (fUpdate)
                            return Request.CreateResponse(HttpStatusCode.OK, objUnidade);
                        else
                            return Request.CreateResponse(HttpStatusCode.OK, new Unidade());
                    }
                    else
                    {
                        var fInsert = _UnidadeAppServices.InsertUnidade(objUnidade);
                        return Request.CreateResponse(HttpStatusCode.OK, fInsert);
                    }
                }
                catch (Exception e)
                {
                    RepositoryBase.salvaLog(objUnidade.Log,  e.Message + " " + e.InnerException + "\n" + e.StackTrace + "\n" + "\n");
                    var message = string.Format(e.Message + " " + e.InnerException + "\n" + e.StackTrace + "\n" + "\n");
                    HttpError err = new HttpError(message);
                    return Request.CreateResponse(HttpStatusCode.NotFound, err);
                }
            }
            else
            {
                return Request.CreateResponse(HttpStatusCode.OK, objUnidade);
            }
        }

