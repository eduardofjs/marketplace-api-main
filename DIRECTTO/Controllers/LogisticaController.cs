        [Route("api/Logistica/CadastroLogistica")]
        [HttpPut]
        public HttpResponseMessage CadastroLogistica(Logistica objLogistica)
        {
            if (ModelState.IsValid)
            {
                try
                {
                    if (objLogistica.LGT_Id > 0)
                    {
                        var fUpdate = _LogisticaAppServices.UpdateLogistica(objLogistica);
                        if (fUpdate)
                            return Request.CreateResponse(HttpStatusCode.OK, objLogistica);
                        else
                            return Request.CreateResponse(HttpStatusCode.OK, new Logistica());
                    }
                    else
                    {
                        var fInsert = _LogisticaAppServices.InsertLogistica(objLogistica);
                        return Request.CreateResponse(HttpStatusCode.OK, fInsert);
                    }
                }
                catch (Exception e)
                {
                    RepositoryBase.salvaLog(objLogistica.Log, e.Message + " " + e.InnerException + "\n" + e.StackTrace + "\n" + "\n");
                    var message = string.Format(e.Message + " " + e.InnerException + "\n" + e.StackTrace + "\n" + "\n");
                    HttpError err = new HttpError(message);
                    return Request.CreateResponse(HttpStatusCode.NotFound, err);
                }
            }
            else
            {
                return Request.CreateResponse(HttpStatusCode.OK, objLogistica);
            }
        }

